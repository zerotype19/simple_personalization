#!/usr/bin/env node
/**
 * Generates integration + webmaster demo docs from:
 * - packages/shared/src/integrationDocs/signal-catalog.json (field definitions)
 * - packages/shared/src/context-packs/index.json (bundled pack manifest)
 * - packages/shared/src/context-packs/activation-mappings/*.json (vendor stubs)
 *
 * Run from repo root: pnpm docs:generate
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

const CATALOG_PATH = path.join(
  ROOT,
  "packages/shared/src/integrationDocs/signal-catalog.json",
);
const CONTEXT_INDEX = path.join(ROOT, "packages/shared/src/context-packs/index.json");
const ACTIVATION_MAP_DIR = path.join(ROOT, "packages/shared/src/context-packs/activation-mappings");
const OUT_DICTIONARY = path.join(ROOT, "docs/session-intelligence-data-dictionary.md");
const OUT_WEBMASTER = path.join(ROOT, "docs/session-intelligence-webmaster-demo-guide.md");

function readJson(p) {
  return JSON.parse(fs.readFileSync(p, "utf8"));
}

function escCell(s) {
  return String(s).replace(/\|/g, "\\|").replace(/\n/g, " ");
}

function table(headers, rows) {
  const h = `| ${headers.map(escCell).join(" | ")} |`;
  const sep = `| ${headers.map(() => "---").join(" | ")} |`;
  const b = rows.map((r) => `| ${r.map(escCell).join(" | ")} |`).join("\n");
  return `${h}\n${sep}\n${b}\n`;
}

function catalogRows(rows, pathPrefix) {
  return rows.map((r) => [
    pathPrefix ? `${pathPrefix}.${r.field}` : r.field,
    r.type,
    r.description,
    Array.isArray(r.destinations) ? r.destinations.join(", ") : r.destinations,
  ]);
}

function main() {
  const catalog = readJson(CATALOG_PATH);
  const ctxIndex = readJson(CONTEXT_INDEX);
  const generatedAt = new Date().toISOString();

  const mappingFiles = fs
    .readdirSync(ACTIVATION_MAP_DIR)
    .filter((f) => f.endsWith(".json"))
    .sort();

  const mappingSections = mappingFiles.map((file) => {
    const data = readJson(path.join(ACTIVATION_MAP_DIR, file));
    let body = "```json\n" + JSON.stringify(data, null, 2) + "\n```\n";
    if (data.fields && typeof data.fields === "object") {
      const rows = Object.entries(data.fields).map(([src, dest]) => [src, dest, "Suggested flat key for Adobe-style mappings."]);
      body += "\n**Field map (suggested):**\n\n";
      body += table(["Source path", "Target key", "Note"], rows);
    }
    return `### ${file}\n\n${body}`;
  });

  const packLines = [];
  for (const key of ["taxonomies", "verticals", "concepts", "stopwords"]) {
    const arr = ctxIndex[key];
    if (!Array.isArray(arr)) continue;
    packLines.push(`- **${key}**`);
    for (const rel of arr) {
      packLines.push(`  - [\`packages/shared/src/context-packs/${rel}\`](../packages/shared/src/context-packs/${rel})`);
    }
  }

  const dict = `# Session Intelligence — integration data dictionary

<!-- Generated by scripts/generate-session-intelligence-docs.mjs at ${generatedAt}. Do not edit by hand; run \`pnpm docs:generate\`. -->

This document lists **signal fields**, **meanings**, and **where each can be pushed** (tag managers, Adobe, Optimizely, custom events, or the Worker collect path). Types and shapes follow \`@si/shared\` and \`packages/sdk/src/siteSemantics/activationPayload.ts\`.

## Source files

| Role | Path |
| --- | --- |
| Signal catalog (this doc’s rows) | [\`packages/shared/src/integrationDocs/signal-catalog.json\`](../packages/shared/src/integrationDocs/signal-catalog.json) |
| Activation envelope builder | [\`packages/sdk/src/siteSemantics/activationPayload.ts\`](../packages/sdk/src/siteSemantics/activationPayload.ts) |
| Shared types | [\`packages/shared/src/index.ts\`](../packages/shared/src/index.ts) |
| Collect payload | [\`packages/sdk/src/batcher.ts\`](../packages/sdk/src/batcher.ts) → \`AnalyticsPayload.summary\` |
| Worker ingest | [\`worker/src/index.ts\`](../worker/src/index.ts) |

## PersonalizationSignal (flat)

Returned by \`SessionIntel.getPersonalizationSignal()\` and mirrored under \`getActivationPayload().si.personalization_signal\`.

${table(["Field", "Type", "Meaning", "Where it can be pushed"], catalogRows(catalog.personalization_signal))}

## Activation payload envelope

Top-level object from \`SessionIntel.getActivationPayload()\`.

${table(["Field", "Type", "Meaning", "Where it can be pushed"], catalogRows(catalog.activation_envelope))}

**Note:** \`si.nba\` may be \`null\` when no recommendation is active. \`si.personalization_signal\` duplicates the **PersonalizationSignal** table above.

## Nested \`si.site\`

${table(["Field", "Type", "Meaning", "Where it can be pushed"], catalogRows(catalog.si_site, "si.site"))}

## Nested \`si.page\`

${table(["Field", "Type", "Meaning", "Where it can be pushed"], catalogRows(catalog.si_page, "si.page"))}

## Nested \`si.session\`

${table(["Field", "Type", "Meaning", "Where it can be pushed"], catalogRows(catalog.si_session, "si.session"))}

## Nested \`si.concepts[]\` (each item)

${table(["Field", "Type", "Meaning", "Where it can be pushed"], catalogRows(catalog.si_concepts_item, "si.concepts[]"))}

## Nested \`si.objective\`

${table(["Field", "Type", "Meaning", "Where it can be pushed"], catalogRows(catalog.si_objective, "si.objective"))}

## Nested \`si.nba\` (when non-null)

${table(["Field", "Type", "Meaning", "Where it can be pushed"], catalogRows(catalog.si_nba_item, "si.nba"))}

## Nested \`si.activation\` (ActivationOpportunity)

${table(["Field", "Type", "Meaning", "Where it can be pushed"], catalogRows(catalog.si_activation_opportunity, "si.activation"))}

## Worker \`/collect\` — \`summary\` JSON (D1 \`summary_json\`)

Only \`payload.summary\` is persisted as JSON on ingest (plus denormalized columns). Full \`AnalyticsPayload\` includes experiment/treatments/conversion flags on the parent object.

${table(["Field", "Type", "Meaning", "Where it can be pushed"], catalogRows(catalog.collect_summary))}

## \`window.SessionIntel\` helpers

${table(["API", "Description", "Typical destination"], catalog.sdk_surface.map((r) => [r.name, r.description, r.destinations]))}

## Browser custom events

${table(["Event", "Description", "Typical destination"], catalog.browser_events.map((r) => [r.name, r.description, r.destinations]))}

## File-based context packs (bundled intelligence)

Manifest: [\`packages/shared/src/context-packs/index.json\`](../packages/shared/src/context-packs/index.json).

${packLines.join("\n")}

## Activation mapping stubs (JSON)

Reference maps under \`packages/shared/src/context-packs/activation-mappings/\` (extend for Launch / AEP / vendor-specific flattening).

${mappingSections.join("\n")}
`;

  const webmaster = `# Session Intelligence — webmaster demo guide

<!-- Generated by scripts/generate-session-intelligence-docs.mjs at ${generatedAt}. Do not edit by hand; run \`pnpm docs:generate\`. -->

Plain-English flow for **anonymous visitor signals** and **activation** (not site redesign). For CSP, snippet hosting, and inspector quirks, see [\`docs/SNIPPET_HOSTING.md\`](SNIPPET_HOSTING.md).

## 1. Install the tag

Add the hosted snippet **near the end of \`<body>\`** when possible:

\`\`\`html
<script async src="https://optiview.ai/si.js"></script>
\`\`\`

Use your own snippet host if the script is deployed elsewhere. The script must be allowed to **\`fetch\`** your Session Intelligence **Worker** origin for \`/config\` and \`/collect\` (see SNIPPET_HOSTING CSP table).

## 2. Open the panel

- Look for the **SI** control (bottom-left) after load, or use **Ctrl+Shift+Backtick** (Windows/Linux) / **⌘+Shift+Backtick** (Mac).
- For demos: append **\`?si_debug=1\`** and **hard-reload** so the drawer can open on first paint, or set \`sessionStorage.setItem('si:debug','1')\` and reload.
- Optional: \`<script src="…/si.js" data-inspector="1" async></script>\` to always enable the inspector on that embed.

In the panel, read **anonymous visitor read**, **inferred need**, **recommended personalization signal**, and the **activation payload** preview.

## 3. Read the personalization signal in code

After the tag has booted:

\`\`\`js
const signal = window.SessionIntel.getPersonalizationSignal();
console.log(signal.inferred_need, signal.recommended_surface, signal.top_concepts);
\`\`\`

Subscribe to live profile updates:

\`\`\`js
window.SessionIntel.subscribe((profile) => {
  console.log(profile.personalization_signal, profile.activation_opportunity);
});
\`\`\`

## 4. Push to Adobe, Optimizely, or Google dataLayer

**One-shot (demo / integration harness):** push the full envelope everywhere and fire browser events:

\`\`\`js
window.SessionIntel.pushPersonalizationSignalAll();
\`\`\`

**À la carte** (same payload as short \`pushTo*\` methods):

\`\`\`js
window.SessionIntel.pushPersonalizationSignalToDataLayer();
window.SessionIntel.pushPersonalizationSignalToAdobeDataLayer();
window.SessionIntel.pushPersonalizationSignalToOptimizely();
\`\`\`

Or listen without tag-manager pushes:

\`\`\`js
window.addEventListener("si:personalization-signal", (e) => {
  const envelope = e.detail; // { event, si }
  // map envelope.si to your CMS slot, DMP, or personalization API
});
\`\`\`

## 5. Use the signal for treatments

The SDK **does not** auto-open popups or change prices. **You** decide what to show:

| Goal | Example using \`signal\` |
| --- | --- |
| Soft popup / interstitial | If \`recommended_surface\` matches your heuristic and \`recommended_timing\` is satisfied (scroll depth, second page — your rules), open a modal with copy aligned to \`recommended_message_angle\`. |
| Offer / coupon / rebate | Map \`journey_stage\` + \`inferred_need\` to a promo tier in your commerce or CDP rules. |
| Guide / checklist / lead module | Use \`recommended_offer_type\` and \`recommended_friction_level\` to pick asset depth and form length. |
| Inline CTA | Render a slot component when \`subscribe\` updates and \`recommended_surface\` is \`primary_cta\` or \`lead_form\`. |

Example sketch:

\`\`\`js
window.SessionIntel.subscribe((profile) => {
  const s = profile.personalization_signal;
  if (!s || s.confidence < 0.5) return;
  if (s.recommended_surface === "primary_cta" && profile.signals.max_scroll_depth > 60) {
    // showInlineGuide(s.recommended_message_angle);
  }
});
\`\`\`

## 6. Privacy posture (demo talking points)

- **Anonymous session** in **sessionStorage** (key \`si:session\`), not a cross-site identity graph.
- **No embeddings, no vector DB, no third-party model calls** in the default tag path.
- **Collect** sends summarized scores and category affinity — not raw page HTML. Inspect \`AnalyticsPayload\` in \`packages/sdk/src/batcher.ts\` for the exact summary fields.

## Related generated doc

- Field-by-field dictionary: [\`session-intelligence-data-dictionary.md\`](session-intelligence-data-dictionary.md)
`;

  fs.writeFileSync(OUT_DICTIONARY, dict, "utf8");
  fs.writeFileSync(OUT_WEBMASTER, webmaster, "utf8");
  console.log(`Wrote ${path.relative(ROOT, OUT_DICTIONARY)}`);
  console.log(`Wrote ${path.relative(ROOT, OUT_WEBMASTER)}`);
}

main();
